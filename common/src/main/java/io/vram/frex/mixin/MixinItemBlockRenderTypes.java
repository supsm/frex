package io.vram.frex.mixin;

import java.util.Map;
import net.minecraft.client.renderer.ItemBlockRenderTypes;
import net.minecraft.client.renderer.RenderType;
import net.minecraft.world.level.block.Block;
import net.minecraft.world.level.material.Fluid;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;
import io.vram.frex.impl.material.BlockPresetsImpl;

@Mixin(ItemBlockRenderTypes.class)
public abstract class MixinItemBlockRenderTypes {
	@Shadow private static Map<Block, RenderType> TYPE_BY_BLOCK;
	@Shadow private static Map<Fluid, RenderType> TYPE_BY_FLUID;

	@Inject(method = "<clinit>*", at = @At("RETURN"))
	private static void onInitialize(CallbackInfo info) {
		BlockPresetsImpl.setConsumers(TYPE_BY_BLOCK::put, TYPE_BY_FLUID::put);
	}
}
